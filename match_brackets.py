def match_brackets(s: str) -> bool:
    """
    判断字符串中的圆括号 (), 方括号 [], 大括号 {} 是否完全匹配。

    匹配规则:
        1. 每种左括号必须有对应同类型的右括号与之配对。
        2. 括号嵌套顺序必须正确 —— 后开的括号必须先闭合。
        3. 非括号字符不影响判断。

    算法: 栈 (Stack)  O(n) 时间 | O(n) 空间
        遍历字符串，遇到左括号则入栈；遇到右括号则检查栈顶
        是否为同类型左括号 —— 是则弹出，否则匹配失败。
        遍历结束后栈为空才算完全匹配。

    参数:
        s: 输入字符串，可包含任意字符。

    返回:
        True  - 括号完全匹配
        False - 括号不匹配 (类型不对 / 顺序错误 / 多余左括号 / 多余右括号)
    """
    # 右括号到对应左括号的映射
    PAIRS = {")": "(", "]": "[", "}": "{"}

    stack: list[str] = []

    for ch in s:
        if ch in ("(", "[", "{"):
            # 左括号: 入栈，等待后续匹配
            stack.append(ch)
        elif ch in (")", "]", "}"):
            # 右括号: 栈为空或栈顶类型不匹配则失败
            if not stack or stack[-1] != PAIRS[ch]:
                return False
            # 匹配成功，弹出栈顶左括号
            stack.pop()
        # 非括号字符直接忽略

    # 栈为空 = 所有左括号均已匹配闭合
    return len(stack) == 0


