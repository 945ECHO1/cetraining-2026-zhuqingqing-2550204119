# 测试用例
# ============================================================
def run_tests():
    """
    运行多组测试用例，覆盖各种场景:
        - 正常匹配
        - 类型不匹配
        - 顺序错误 (交叉嵌套)
        - 多余左括号
        - 多余右括号
        - 空字符串
        - 无括号字符串
        - 含非括号字符的表达式
        - 边界情况
    """
    test_cases = [
        # ---- 有效匹配 ----
        ("",            True,   "空字符串"),
        ("()",          True,   "单对圆括号"),
        ("[]",          True,   "单对方括号"),
        ("{}",          True,   "单对大括号"),
        ("()[]{}",      True,   "多对并列"),
        ("({[]})",      True,   "三层嵌套"),
        ("({})[]",      True,   "嵌套并列混合"),
        ("(((())))",    True,   "深层嵌套"),
        ("abc",         True,   "无括号字符串"),

        # ---- 类型不匹配 ----
        ("(]",          False,  "圆括号遇方括号闭合"),
        ("[)",          False,  "方括号遇圆括号闭合"),
        ("{]",          False,  "大括号遇方括号闭合"),

        # ---- 顺序错误 (交叉嵌套) ----
        ("([)]",        False,  "交叉嵌套"),
        ("{[}]",        False,  "大括号交叉"),
        ("[(])",        False,  "方括号交叉"),

        # ---- 多余左括号 ----
        ("(((",         False,  "只有左括号未闭合"),
        ("()(",         False,  "一对正确后多余一个左括号"),
        ("{[()]",       False,  "缺一个右大括号"),

        # ---- 多余右括号 ----
        (")))",         False,  "只有右括号"),
        ("())",         False,  "一对正确后多余右括号"),
        ("()}",         False,  "多余右大括号"),

        # ---- 含非括号字符 (编程表达式) ----
        ("a + (b * [c - {d}])",             True,   "算术表达式"),
        ("def f(): return [1, {2: (3+4)}]", True,   "Python 代码片段"),
        ("if (x > 0) { print([x]) }",       True,   "类 C 伪代码"),

        # ---- 边界 ----
        ("{[()]}",      True,   "完全嵌套"),
        ("{[(])}",      False,  "中间层类型交叉"),
    ]

    passed = 0
    failed = 0

    for s, expected, desc in test_cases:
        result = match_brackets(s)
        if result == expected:
            passed += 1
            status = "PASS"
        else:
            failed += 1
            status = "FAIL"

        print(f"[{status}] {desc}")
        print(f"        输入: {repr(s)}")
        print(f"        结果: {result}  期望: {expected}")
        print()

    print(f"=" * 50)
    print(f"总计: {passed} 通过, {failed} 失败 (共 {len(test_cases)} 项)")


if __name__ == "__main__":
    run_tests()